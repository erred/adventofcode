package main

import "fmt"

func main() {
	sum := make([]int, 12)
	for _, row := range input {
		for i := range row {
			sum[i] += row[i]
		}
	}

	var gamma, epsilon int
	t := 1
	for i := 11; i >= 0; i-- {
		var g, e int
		if sum[i] > len(input)/2 {
			g = 1
		} else {
			e = 1
		}
		gamma += g * t
		epsilon += e * t
		t *= 2
	}
	fmt.Println(gamma * epsilon)

	oxygen, co2 := make([][]int, len(input)), make([][]int, len(input))
	copy(oxygen, input)
	for i := 0; i < 12; i++ {
		if len(oxygen) == 1 {
			break
		}
		var colSum int
		for j := range oxygen {
			colSum += oxygen[j][i]
		}
		var toKeep int
		if colSum >= len(oxygen)-colSum {
			toKeep = 1
		}
		for j := 0; j < len(oxygen); {
			if oxygen[j][i] == toKeep {
				j++
				continue
			}
			oxygen[j] = oxygen[len(oxygen)-1]
			oxygen = oxygen[:len(oxygen)-1]
		}
	}
	copy(co2, input)
	for i := 0; i < 12; i++ {
		if len(co2) == 1 {
			break
		}
		var colSum int
		for j := range co2 {
			colSum += co2[j][i]
		}
		var toKeep int
		if colSum < len(co2)-colSum {
			toKeep = 1
		}
		if colSum == 0 || colSum == len(co2) {
			continue
		}
		for j := 0; j < len(co2); {
			if co2[j][i] == toKeep {
				j++
				continue
			}
			co2[j] = co2[len(co2)-1]
			co2 = co2[:len(co2)-1]
		}
	}

	var out1, out2 int
	t = 1
	for i := 11; i >= 0; i-- {
		out1 += oxygen[0][i] * t
		out2 += co2[0][i] * t
		t *= 2
	}
	fmt.Println(out1 * out2)
}

